#! /bin/bash
# Sagedusloend, kus ma-vormis tegusõnale järgneb adverb.

sed 's/^\([^\/]*\/\/[^\/]*\/\/\).*/\1/g' \
| tr '\n' ' ' \
| sed 's/\(\/\/ \)\([[:lower:]]*\+[^\/]*\/\/_D_ \/\/ [^ ]*    [^\/]*\/\/_V_[^\/]*\/\/\)/\1£\2£/g' \
| sed 's/\([^£]*\)\(£[^£]*\)\([^£]*\)\(£\)/\2\4/g' \
| tr '£' '\n' \
| grep -v 'ei+0 //_V_ neg, //' \
| sed 's/^\([^ ]*\)\(    [^\/]*\/\/[^\/]*\/\/ [^ ]*\)\(    \)/\1\3/g' \
| tr 'A-Z' 'a-z' \
| grep -v '^$' \
| sed 's/\([^\+]*\)\+.*$/\1ma/g' \
| sed 's/^\([^_]*\)_\(.*\)$/\1\2/g' \
| sed 's/^\([^ ]*\)    \(.*\)$/\1 \2/g' \
| sed 's/^\([^ ]*\)\( \)\(.*\)/\3\2\1/g' \
| grep -v 'kesma' \
| sort | uniq -c | sort -nr